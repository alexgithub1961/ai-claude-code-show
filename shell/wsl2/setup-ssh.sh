#!/usr/bin/env bash
#
# setup-ssh.sh - Install and configure OpenSSH server in WSL2
#
# Configures SSH for secure key-based authentication only.
#

set -euo pipefail

# Colours
GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m'

log_info() { echo -e "${BLUE}[INFO]${NC} $1"; }
log_success() { echo -e "${GREEN}[SUCCESS]${NC} $1"; }
log_warn() { echo -e "${YELLOW}[WARN]${NC} $1"; }
log_error() { echo -e "${RED}[ERROR]${NC} $1"; }

echo "========================================"
echo "SSH Server Setup for WSL2"
echo "========================================"
echo ""

# Check if running as root (we need sudo for some operations)
if [[ $EUID -eq 0 ]]; then
    log_error "Do not run this script as root. It will use sudo when needed."
    exit 1
fi

# Install OpenSSH server
log_info "Installing OpenSSH server..."
sudo apt-get update -qq
sudo apt-get install -y openssh-server

# Backup original sshd_config
SSHD_CONFIG="/etc/ssh/sshd_config"
BACKUP_CONFIG="/etc/ssh/sshd_config.backup.$(date +%Y%m%d_%H%M%S)"

if [[ ! -f "${SSHD_CONFIG}.original" ]]; then
    log_info "Backing up original sshd_config..."
    sudo cp "$SSHD_CONFIG" "${SSHD_CONFIG}.original"
fi

# Configure sshd_config for security
log_info "Configuring SSH server..."

# Create a secure configuration
sudo tee /etc/ssh/sshd_config.d/wsl2-secure.conf > /dev/null << 'EOF'
# WSL2 SSH Configuration
# Generated by setup-ssh.sh

# Disable password authentication (key-only)
PasswordAuthentication no
ChallengeResponseAuthentication no

# Enable public key authentication
PubkeyAuthentication yes

# Disable root login
PermitRootLogin no

# Use modern key exchange and ciphers
KexAlgorithms curve25519-sha256,curve25519-sha256@libssh.org
Ciphers chacha20-poly1305@openssh.com,aes256-gcm@openssh.com,aes128-gcm@openssh.com

# Logging
LogLevel INFO

# Connection settings
MaxAuthTries 3
LoginGraceTime 60
ClientAliveInterval 60
ClientAliveCountMax 3
EOF

log_success "SSH configuration updated"

# Generate host keys if they don't exist
log_info "Checking SSH host keys..."
if [[ ! -f /etc/ssh/ssh_host_ed25519_key ]]; then
    log_info "Generating SSH host keys..."
    sudo ssh-keygen -A
fi

# Ensure .ssh directory exists for current user
SSH_DIR="$HOME/.ssh"
AUTH_KEYS="$SSH_DIR/authorized_keys"

if [[ ! -d "$SSH_DIR" ]]; then
    log_info "Creating ~/.ssh directory..."
    mkdir -p "$SSH_DIR"
    chmod 700 "$SSH_DIR"
fi

if [[ ! -f "$AUTH_KEYS" ]]; then
    log_info "Creating authorized_keys file..."
    touch "$AUTH_KEYS"
    chmod 600 "$AUTH_KEYS"
fi

# Start SSH service
log_info "Starting SSH service..."
sudo service ssh restart

# Verify SSH is running
if pgrep -x "sshd" > /dev/null; then
    log_success "SSH server is running"
else
    log_error "SSH server failed to start"
    exit 1
fi

echo ""
echo "========================================"
log_success "SSH Server Setup Complete!"
echo "========================================"
echo ""
echo "SSH is configured for key-based authentication only."
echo ""
echo "To add your public key from Blink (iOS):"
echo "  1. In Blink, go to Settings > Keys"
echo "  2. Create or select an Ed25519 key"
echo "  3. Copy the public key"
echo "  4. Add it to: $AUTH_KEYS"
echo ""
echo "Example (paste your public key):"
echo "  echo 'ssh-ed25519 AAAA...' >> $AUTH_KEYS"
echo ""
echo "Test connection:"
if command -v tailscale &> /dev/null && tailscale status &> /dev/null 2>&1; then
    echo "  ssh $(whoami)@$(tailscale ip -4)"
else
    echo "  ssh $(whoami)@<your-ip-address>"
fi
